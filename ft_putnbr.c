#include "ft_printf.c"

int	ft_putnbr(int nb)
{
	if (nb == -2147483648)
	{
		write(1,"2147483648",11);
		return 2;
	}
	if (nb < 0)
	{
		nb = nb * -1;
	ft_putchar('-');
	}
	else if (nb > 9)
		ft_putnbr(nb / 10);
	ft_putchar(nb % 10 + '0');

	return (2);
}
